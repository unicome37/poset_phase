"""Prediction C — Analytical Bound: Complete Layered Poset Entropy.

For a COMPLETE k-layered poset (all inter-layer edges present, no intra-layer edges),
with equal layer sizes m = N/k, the number of linear extensions is exactly:

    L(N, k) = N! / (m!)^k      (multinomial coefficient)

Therefore:
    log L = log(N!) - k * log((N/k)!)

This gives an EXACT analytical expression for log_H as a function of k at fixed N.

Key insight: this complete layered poset represents a LOWER BOUND on entropy for
posets with k layers and N elements, because it is the maximally constrained
k-layered poset (all possible inter-layer relations are present).

In contrast, SPARSE layered posets (like those generated by generate_random_layered)
have fewer constraints and thus higher entropy. The empirical dose-response curve
should lie ABOVE this analytical lower bound.

This script:
1. Computes the analytical formula
2. Validates against exact counting at small N
3. Overlays analytical curves on empirical data
4. Derives the analytical prediction for d(log_H)/dk
"""
