# ══════════════════════════════════════════════════════════════
#  ANALYTICAL FORMULAS
# ══════════════════════════════════════════════════════════════

def log_H_complete_layered(N: int, k: int) -> float:
    """Exact log(linear extensions) for complete k-layered poset with equal layers.

    In a complete k-layered poset, every element in layer i is below every element
    in layer j (for i < j). Elements within the same layer are incomparable.
    A linear extension must list all elements of layer 1 first, then layer 2, etc.
    Within each layer, any permutation is valid.

    Therefore: L = n_1! * n_2! * ... * n_k!
    For equal layers m = N/k: L = (m!)^k
    Hence: log L = k * log(m!)

    This is a DECREASING function of k at fixed N:
    - k=1: log_H = log(N!)  (antichain, maximum entropy)
    - k=N: log_H = 0        (total order, minimum entropy)
    """
    if N % k != 0:
        m = N // k
        remainder = N % k
        sizes = [m + 1] * remainder + [m] * (k - remainder)
        return sum(math.lgamma(s + 1) for s in sizes)
    m = N // k
    return k * math.lgamma(m + 1)


def d_log_H_dk_approx(N: int, k: float) -> float:
    """Approximate derivative d(log_H)/dk for complete layered poset.

    log_H = k * log((N/k)!)

    Using Stirling: log(m!) ≈ m*log(m) - m + 0.5*log(2πm)
    Let m = N/k, dm/dk = -N/k^2 = -m/k

    d(log_H)/dk = log(m!) + k * d[log(m!)]/dk
                = log(m!) + k * ψ(m+1) * dm/dk
                = log(m!) - m * ψ(m+1)

    where ψ is the digamma function. Using Stirling:
    ψ(m+1) ≈ log(m) + 1/(2m)
    log(m!) ≈ m*log(m) - m

    So d(log_H)/dk ≈ m*log(m) - m - m*(log(m) + 1/(2m))
                    = -m - 1/2
                    < 0 for all m > 0

    This proves d(log_H)/dk < 0: entropy ALWAYS decreases with more layers.
    """
    m = N / k
    # Exact using lgamma
    log_m_fact = math.lgamma(m + 1)
    # digamma approximation
    psi = math.log(m) + 1 / (2 * m) if m > 0.5 else -0.5772
    return log_m_fact - m * psi


# ══════════════════════════════════════════════════════════════
#  VALIDATION: Check formula against exact counting
# ══════════════════════════════════════════════════════════════

def build_complete_layered_poset(N: int, k: int) -> Poset:
    """Build a complete k-layered poset (all inter-layer edges)."""
    m = N // k
    remainder = N % k
    sizes = [m + 1] * remainder + [m] * (k - remainder)

    adj = np.zeros((N, N), dtype=bool)
    offset = 0
    layer_ranges = []
    for s in sizes:
        layer_ranges.append((offset, offset + s))
        offset += s

    for i in range(len(layer_ranges)):
        for j in range(i + 1, len(layer_ranges)):
            li_start, li_end = layer_ranges[i]
            lj_start, lj_end = layer_ranges[j]
            adj[li_start:li_end, lj_start:lj_end] = True

    return Poset(transitive_closure(adj))


def validate():
    print("=== Validation: Formula vs Exact Counting ===\n")
    errors = []
    for N in [8, 10, 12, 14, 16]:
        for k in [2, 3, 4]:
            if k > N:
                continue
            poset = build_complete_layered_poset(N, k)
            exact_logH = math.log(count_linear_extensions_exact(poset, prefer_c=False))
            formula_logH = log_H_complete_layered(N, k)
            err = abs(exact_logH - formula_logH)
            errors.append(err)
            status = "✓" if err < 0.01 else "✗"
            print(f"  {status} N={N}, k={k}: exact={exact_logH:.4f}, formula={formula_logH:.4f}, err={err:.2e}")

    max_err = max(errors)
    print(f"\n  Max error: {max_err:.2e}")
    print(f"  {'✓ VALIDATED' if max_err < 0.01 else '✗ FAILED'}")
    return max_err < 0.01


