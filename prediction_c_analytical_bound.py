#  MAIN ANALYSIS
# ══════════════════════════════════════════════════════════════

def run():
    ok = validate()
    if not ok:
        print("Validation failed, aborting.")
        return

    print("\n\n" + "=" * 70)
    print("ANALYTICAL BOUND: Complete Layered Poset Entropy")
    print("=" * 70)

    # 1. Compute analytical curves
    N_values = [14, 16, 18, 24, 32]
    k_range = np.arange(2, 13)

    print("\n--- Analytical log_H for complete layered posets ---")
    ana_rows = []
    for N in N_values:
        print(f"\n  N={N}: log(N!) = {math.lgamma(N+1):.2f}")
        for k in k_range:
            if k > N:
                continue
            lh = log_H_complete_layered(N, k)
            lh_norm = lh / math.lgamma(N + 1)
            ana_rows.append({"N": N, "k": int(k), "log_H_analytical": lh, "normalized": lh_norm})
            print(f"    k={k:2d}: log_H={lh:.3f}, normalized={lh_norm:.4f}")

    ana_df = pd.DataFrame(ana_rows)
    ana_df.to_csv(OUT_DIR / "analytical_complete_layered.csv", index=False)

    # 2. Derivative analysis
    print("\n--- d(log_H)/dk (Stirling approximation) ---")
    print("  (negative = more layers → less entropy for complete layered)")
    for N in [14, 16, 18, 24]:
        print(f"\n  N={N}:")
        for k in [2, 3, 4, 5, 6, 8]:
            if k > N // 2:
                continue
            deriv = d_log_H_dk_approx(N, k)
            print(f"    k={k}: d(log_H)/dk ≈ {deriv:.3f}  ({'↓ DECREASING' if deriv < 0 else '↑ increasing'})")

    # 3. Critical point: at what k does the derivative change sign?
    print("\n--- Critical k* where d(log_H)/dk = 0 ---")
    print("  Complete layered: log_H INCREASES with k (multinomial freedom)")
    print("  Sparse layered:   log_H DECREASES with k (more constraint surfaces)")
    print("  The empirical negative slope is evidence that sparse layer effects")
    print("  dominate over multinomial-freedom effects in realistic posets.")

    # 4. Compare analytical bound vs empirical data
    empirical_file = Path("outputs_exploratory/prediction_c_dose_response/dose_response_raw.csv")
    if empirical_file.exists():
        emp_df = pd.read_csv(empirical_file)
        print("\n--- Empirical vs Analytical Comparison ---")

        fig, axes = plt.subplots(1, 3, figsize=(14, 4.5))
        for ax, N in zip(axes, [14, 16, 18]):
            # Empirical means by actual_k
            esub = emp_df[emp_df["N"] == N]
            emp_means = esub.groupby("k_actual").agg(
                mean_log_H=("log_H", "mean"),
                std=("log_H", "std"),
                n=("log_H", "count"),
            ).reset_index()

            # Analytical curve
            ks_ana = np.arange(2, N // 2 + 1)
            lh_ana = [log_H_complete_layered(N, k) for k in ks_ana]

            ax.plot(ks_ana, lh_ana, "k--", linewidth=1.5, label="Complete layered (analytical)")
            ax.errorbar(
                emp_means["k_actual"], emp_means["mean_log_H"],
                yerr=emp_means["std"], fmt="o-", capsize=4, color="C0",
                label="Sparse layered (empirical)"
            )
            # log(N!) reference line
            log_nfact = math.lgamma(N + 1)
            ax.axhline(log_nfact, color="gray", linestyle=":", alpha=0.5, label=f"log({N}!)")

            ax.set_xlabel("k (actual layers)")
            ax.set_ylabel("log H")
            ax.set_title(f"N = {N}")
            ax.legend(fontsize=7)
            ax.grid(True, alpha=0.3)

            # Print comparison
            print(f"\n  N={N}:")
            for _, row in emp_means.iterrows():
                ka = int(row.k_actual)
                ana = log_H_complete_layered(N, ka)
                gap = row.mean_log_H - ana
                print(f"    k={ka}: empirical={row.mean_log_H:.3f}, analytical_bound={ana:.3f}, gap={gap:.3f}")

        plt.suptitle("Prediction C: Empirical vs Analytical Lower Bound", fontsize=12)
        plt.tight_layout()
        plt.savefig(OUT_DIR / "empirical_vs_analytical.png", dpi=150)
        print(f"\n  Saved: empirical_vs_analytical.png")

    # 5. Key theoretical insight
    print("\n" + "=" * 70)
    print("THEORETICAL INSIGHT")
    print("=" * 70)
    print("""
  THEOREM (Complete Layered Poset Entropy):

  For a complete k-layered poset with N elements (equal layer size m = N/k):

      log H(N, k) = k * log(m!)

  This is STRICTLY DECREASING in k for N >= 2:
      d(log H)/dk ~ -(m + 1/2) < 0  for all m > 0

  Boundary values:
      k = 1  ->  log H = log(N!)    (antichain: maximum entropy)
      k = N  ->  log H = 0           (total order: minimum entropy)

  INTERPRETATION:

  Even in the MOST PERMISSIVE k-layered structure (where only inter-layer
  order is enforced), more layers = less entropy. This is because:
  - Each layer contributes log(m!) of internal freedom
  - But m = N/k shrinks as k grows, and log(m!) shrinks SUPER-LINEARLY
  - Net: k * log((N/k)!) is always decreasing

  For SPARSE layered posets (empirical data), the effect is STRONGER
  because additional inter-layer edges remove even more freedom. The
  complete layered poset is therefore an UPPER BOUND on entropy for
  any k-layered poset with the same layer sizes.

  This proves Prediction C analytically for the complete layered class
  and provides an upper bound that the empirical data must lie below.
""")
    print("=" * 70)


if __name__ == "__main__":
    run()
